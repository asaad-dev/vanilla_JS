//la page js pour 01-rectangle
let longueur = 7;
let largeur = 9;
console.log('paramétres du rectangle : longueur ' + longueur + ', largeur ' + largeur);

// Pour calculer l'aire d'un rectangle, il faut multiplier longueur * largeur
let air = longueur * largeur
console.log('L\'air du rectangle est de  ', air);

// Pour calculer le paramètre d'un rectangle, il faut (longueur*2)+(largeur*2)
let perimetre = (longueur * 2) + (largeur * 2);
// perimetre = (7*2)+(9*2)
// perimetre = 14+18
// perimetre = 32
console.log('Le périmètre du rectangle est de', perimetre);